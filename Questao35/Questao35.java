public class Questao35 {
    
    public static int Maior(int x, int y){
        return Math.max(x, y);
    }

    public static double Maior(double x, double y){
        return Math.max(x, y);
    }

    public static int Maior(int x, int y, int z){
        return Math.max(x, Math.max(y, z));
    }

    public static double Maior(double x, double y, double z){
        return Math.max(x, Math.max(y, z));
    }

    public static int Maior(int x, int y, int z, int w){
        return Math.max(x, Math.max(y, Math.max(z, w)));
    }

    public static double Maior(double x, double y, double z, double w){
        return Math.max(x, Math.max(y, Math.max(z, w)));
    }

}
